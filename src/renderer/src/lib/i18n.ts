const catalogs: Record<Locale, Partial<Record<TranslationKey, string>>> = {
  en,
  "zh-CN": zhCN,
};

export function resolveLocale(
  preference: LanguagePreference,
  systemLocales: readonly string[],
): Locale {
  if (preference !== "system") return preference;
  return systemLocales.some((locale) => locale.toLowerCase().startsWith("zh"))
    ? "zh-CN"
    : "en";
}

export function translate(
  locale: Locale,
  key: TranslationKey | string,
  values: Record<string, string | number> = {},
): string {
  const message = catalogs[locale][key as TranslationKey] ?? en[key as TranslationKey] ?? key;
  return message.replace(/\{(\w+)\}/g, (_, name: string) => String(values[name] ?? `{${name}}`));
}

export function useI18n() {
  const settings = useSettings();
  const preference = settings?.appearance.language ?? "system";
  const locale = useMemo(
    () => resolveLocale(preference, systemLanguageList()),
    [preference],
  );
  const t = useCallback(
    (key: TranslationKey, values?: Record<string, string | number>) =>
      translate(locale, key, values),
    [locale],
  );

  useEffect(() => {
    document.documentElement.lang = locale;
  }, [locale]);

  return { locale, preference, t } as const;
}

function systemLanguageList(): readonly string[] {
  if (typeof navigator === "undefined") return ["en"];
  return navigator.languages?.length ? navigator.languages : [navigator.language || "en"];
}
